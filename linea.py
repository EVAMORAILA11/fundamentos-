import turtle

# Línea punteada hacia la derecha
turtle.pendown()
turtle.forward(5)
turtle.penup()
turtle.forward(5)

turtle.pendown()
turtle.forward(5)
turtle.penup()
turtle.forward(5)

turtle.pendown()
turtle.forward(5)
turtle.penup()
turtle.forward(5)

# Gira a la izquierda (por ejemplo, 90 grados)
turtle.left(90)

# Línea punteada hacia arriba
turtle.pendown()
turtle.forward(5)
turtle.penup()
turtle.forward(5)

turtle.pendown()
turtle.forward(5)
turtle.penup()
turtle.forward(5)

turtle.done()
